/** Compact themed vocabulary: [word, translation, difficulty?, opts?] */
function bulk(category, rows, defaultDiff = 'easy') {
  return rows.map(([word, translation, diff, opts = {}]) =>
    v(word, translation, {
      category,
      difficulty: diff ?? defaultDiff,
      ...opts,
    }),
  )
}

